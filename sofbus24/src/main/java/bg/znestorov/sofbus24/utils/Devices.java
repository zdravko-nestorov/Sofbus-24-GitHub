    private static String sDeviceName;

    /**
     * Returns the consumer friendly device name
     *
     * @return the device name
     */
    public static String getDeviceName() {
        if (sDeviceName != null) {
            return sDeviceName;
        }

        String key = Build.DEVICE + "|" + Build.MODEL;
        for (String str : SUPPORTED_DEVICES) {
            if (str.endsWith(key)) {
                String[] names = str.split("\\|");
                String retailBranding = names[0];
                String marketingName = names[1];

                if (marketingName.startsWith(retailBranding)) {
                    sDeviceName = marketingName;
                } else {
                    sDeviceName = retailBranding + " " + marketingName;
                }

                return sDeviceName;
            }
        }

        String manufacturer = Build.MANUFACTURER;
        String model = Build.MODEL;
        if (model.startsWith(manufacturer)) {
            sDeviceName = capitalize(model);
        } else if (manufacturer.equalsIgnoreCase("HTC")) {
            sDeviceName = "HTC " + model; // make sure HTC is capitalized
        } else {
            sDeviceName = capitalize(manufacturer) + " " + model;
        }

        return sDeviceName;
    }

    /**
     * Returns the consumer friendly device name based on the value of
     * {@link Build#DEVICE}
     *
     * @param device the device
     * @return the device name
     */
    public static String getDeviceName(String device) {
        String key = "|" + device + "|";

        for (String str : SUPPORTED_DEVICES) {
            if (str.contains(key)) {
                String[] names = str.split("\\|");
                String retailBranding = names[0];
                String marketingName = names[1];

                if (marketingName.startsWith(retailBranding)
                        || marketingName.toLowerCase(Locale.ENGLISH)
                        .startsWith("moto ")) {
                    return marketingName;
                }

                return retailBranding + " " + marketingName;
            }
        }

        return null;
    }

    /**
     * Capitalize the charecters of the string input
     *
     * @param str the string input
     * @return the capitalized string
     */
    private static String capitalize(String str) {
        if (TextUtils.isEmpty(str)) {
            return str;
        }

        char[] arr = str.toCharArray();
        boolean capitalizeNext = true;
        String phrase = "";

        for (char c : arr) {
            if (capitalizeNext && Character.isLetter(c)) {
                phrase += Character.toUpperCase(c);
                capitalizeNext = false;
                continue;
            } else if (Character.isWhitespace(c)) {
                capitalizeNext = true;
            }

            phrase += c;
        }

        return phrase;
    }

